#include <stddef.h>

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*mem;
	size_t			i;

	mem = (unsigned char *)s;
	i = 0;
	while (i < n && mem[i] != (unsigned char)c)
		++i;
	if (i < n)
		return (mem + i);
	return (NULL);
}
